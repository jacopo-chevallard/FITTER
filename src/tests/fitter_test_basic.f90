!< FITTER test.
program fitter_test_basic
!-----------------------------------------------------------------------------------------------------------------------------------
!< FITTER test.
!-----------------------------------------------------------------------------------------------------------------------------------
use, intrinsic :: iso_fortran_env, only : real64
use fitter
!-----------------------------------------------------------------------------------------------------------------------------------

!-----------------------------------------------------------------------------------------------------------------------------------
implicit none
type(timer) :: chronos ! The timer.
!-----------------------------------------------------------------------------------------------------------------------------------

!-----------------------------------------------------------------------------------------------------------------------------------
call chronos%start
call foo
call chronos%stop

call chronos%start
call bar
call chronos%stop

call chronos%start
call bar
call chronos%stop

call chronos%print(statistics=.true.)
!-----------------------------------------------------------------------------------------------------------------------------------
contains
  subroutine foo
  !---------------------------------------------------------------------------------------------------------------------------------
  !< Waste some times.
  !---------------------------------------------------------------------------------------------------------------------------------
  integer :: i !< Counter.
  real    :: r !< Dummy real.
  !---------------------------------------------------------------------------------------------------------------------------------

  !---------------------------------------------------------------------------------------------------------------------------------
  do i=1, 100000
    r = i**0.3
  enddo
  !---------------------------------------------------------------------------------------------------------------------------------
  endsubroutine foo

  subroutine bar
  !---------------------------------------------------------------------------------------------------------------------------------
  !< Waste some more times.
  !---------------------------------------------------------------------------------------------------------------------------------
  integer :: i !< Counter.
  real    :: r !< Dummy real.
  !---------------------------------------------------------------------------------------------------------------------------------

  !---------------------------------------------------------------------------------------------------------------------------------
  do i=1, 150000
    r = i**0.3
  enddo
  !---------------------------------------------------------------------------------------------------------------------------------
  endsubroutine bar
endprogram fitter_test_basic
